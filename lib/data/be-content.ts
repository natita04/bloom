export function getBeContentForWeek(week: number): BeContent {
  const exact = beContentByWeek.find(c => c.week === week);
  if (exact) return exact;
  // Find closest week
  return beContentByWeek.reduce((prev, curr) =>
    Math.abs(curr.week - week) < Math.abs(prev.week - week) ? curr : prev
  );
}

